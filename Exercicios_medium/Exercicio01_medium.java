package Exercicios_medium;
public class Exercicio01_medium {

public void verificarParEImpar () {
    int i = 0;

    while (i <= 100) {
    
        if (i%2==0) {
            System.out.println ("O número: " + i + " é par!");
        } // fim if

        else {
        System.out.println ("O número: " + i + " é impar!");
        } // fim else

        i++;
    } // fim while



} //fim do main

} // fim da class